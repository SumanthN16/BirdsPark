    <script>
        // Function to filter the birds based on the search input
        function filterBirds() {
            const searchInput = document.getElementById('birdSearch').value.toLowerCase();
            const galleryItems = document.querySelectorAll('.gallery-item');

            galleryItems.forEach(item => {
                const birdName = item.querySelector('img').alt.toLowerCase();

                if (birdName.includes(searchInput)) {
                    item.style.display = 'block';
                } else {
                    item.style.display = 'none';
                }
            });
        }

        // Event listener to trigger filtering on search input change
        document.getElementById('birdSearch').addEventListener('input', filterBirds);
    </script>
